/*

=> SQL: Stands for Structured Query Language, and was designed to query and manage data
		in relational database management system (RDBMS).

=> RDBMS: is a database management system based on the relational mode, which in turn
		  is based on two mathematical branches: SET THEORY and PREDICATE LOGIC.

=> SQL uses a declarative paradigm, that is requires you to specify 
   what you want to get and not how to get it.

=> SQL Categories:
	- Data Definition Language (DDL): 
		* which deals with object definitions and include statement such as CREATE, ALTER and DROP.

	- Data Manipulation Language (DML):
		* which allows you to query and modify data and include statements such as 
		  SELECT, INSERT, UPDATE, DELETE, TRUNCATE, and MERGE.

	- Data Control Language (DCL):
		* which deals with permissions and include statements such as GRANT and REVOKE


=> SET Theory: by "set" we mean any collection M into a whole of definition, distinct objects m
			   (which are called the elements of M) of our perception or of our though.

	- You should think of the set of employees as a whole rather than the individual employees.
	- The word distinct means that every element of a set must be unique.
	- The phrase of our perception or of our thought implies that the definition of a set is subjective.
		* Consider a classroom: One person might perceive a set of people, whereas another might perceive a
		  set of students and a set of teachers.
	- An element is supposed to be identified by name not by ordinal position.

=> PREDICATE LOGIC: a predicate is a property or an expression that either holds or doesn’t hold. 
				    in other words, is either true or false.
	- The relational model relies on predicates to maintain the logical integrity of the data and define
	  its structure.
	- You can also use predicates when filtering data to define subsets, and more.
	- In set theory, you can use predicates to define sets. This is helpful because you can’t always define
	  a set by listing all its elements.

=> The Relational Model: The relational model is a semantic model for data management and manipulation and 
						 is based on set theory and predicate logic.
	- The goal of the relational model is to enable consistent representation of data with minimal or no
	  redundancy and without sacrificing completeness, and to define data integrity

*/